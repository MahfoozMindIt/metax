    let activeContentId = 'europe-collect-content';
    function toggleButton(button) {
        // Get both buttons
        const collectBtn = document.getElementById("collect-btn");
        const payBtn = document.getElementById("pay-btn");

        // If 'Collect' button is clicked
        if (button === 'collect') {
            collectBtn.classList.add('active-btn');
            collectBtn.classList.remove('non-active-btn');
            payBtn.classList.add('non-active-btn');
            payBtn.classList.remove('active-btn');
        }

        // If 'Pay' button is clicked
        if (button === 'pay') {
            payBtn.classList.add('active-btn');
            payBtn.classList.remove('non-active-btn');
            collectBtn.classList.add('non-active-btn');
            collectBtn.classList.remove('active-btn');
        }

        refreshContent();
    }



    function showContent(region, index) {
        // Hide all content
        document.getElementById('europe-collect-content').style.display = 'none';
        document.getElementById('mea-collect-content').style.display = 'none';
        document.getElementById('america-collect-content').style.display = 'none';
        document.getElementById('asia-collect-content').style.display = 'none';
        document.getElementById('africa-collect-content').style.display = 'none';

        // Show the selected content
        document.getElementById(region).classList.remove('d-none');
        document.getElementById('europe').classList.remove('active-list-text');
        document.getElementById('mea').classList.remove('active-list-text');
        document.getElementById('america').classList.remove('active-list-text');
        document.getElementById('asia').classList.remove('active-list-text');
        document.getElementById('africa').classList.remove('active-list-text');

        document.getElementById('europe').classList.add('list-text');
        document.getElementById('mea').classList.add('list-text');
        document.getElementById('america').classList.add('list-text');
        document.getElementById('asia').classList.add('list-text');
        document.getElementById('africa').classList.add('list-text');
        document.getElementById(region).style.display = 'flex';
        activeContentId = region;

        switch (index) {
            case 0:
                document.getElementById('europe').classList.add('active-list-text');

                document.getElementById('europe').classList.remove('list-text');
                break;
            case 1:
                document.getElementById('mea').classList.add('active-list-text');

