                document.getElementById('africa').classList.remove('list-text');
                break;
        }

    }

    function refreshContent() {
        const content = document.getElementById(activeContentId);
        const list = document.getElementById('region-list')

        // Hide the content temporarily
        list.style.display = 'none';
        content.style.display = 'none';


        // After a short delay, show it again to simulate refresh animation
        setTimeout(() => {

            list.style.display = 'flex';
        }, 100);
        setTimeout(() => {
            content.style.display = 'flex';
            list.style.display = 'flex';
        }, 100); // Adjust the timeout as per the refresh effect you want
    }


