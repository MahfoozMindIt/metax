                            <p>Corporate Documents</p>
                            <ul>
                                <li>Certificate of Incorporation</li>
                                <li>Memorandum and Articles of Association</li>
                                <li>Copy of Register of Directors</li>
                                <li>Copy of Register of Shareholders</li>
                                <li>Certificate of Good Standing / Certificate of Incumbency (where applicable)</li>
                                <li>Copy of Company bank statement (last 3 months)</li>
                            </ul>
                            <p>Individual(s) Documents</p>
                            <ul>
                                <li>Individual verification of all active Directors (POI, POA)</li>
                                <li>Individual verification of Shareholders / Beneficial Owners with a holding of 10% or more (POI, POA)</li>
                                <li></li>
                                <li>Individual verification(s) of All Authorised Users on the Account (POI, POA)</li>
                            </ul>
                            <p>NOTE: Where the Authorised Representative is not a Director of the company, a Director will need to confirm in writing the
                                name and authority of the of the person responsible for the administration of the account and we will need;</p>
                            <ul>
                                <li>Individual verification of Authorised Representative (POI, POA)</li>
                            </ul>
                            <p>NOTE: In instances where the Entity is required to be Licensed and/or Regulated, we need;</p>
                            <ul>
                                <li>Copy of any Licence(s)</li>
                                <li>Copy of Authorisation document from the relevant Regulatory body</li>
                                <li>AML and KYC Policies and a CV for the MLRO or Head of Compliance</li>
                            </ul>
                            <p>NOTE: For Corporate shareholders (where the registered shareholder is another company), we need;</p>
                            <ul>
                                <li>All of the above (1-12) for all Parent companies</li>
                                <li>Organisational Chart</li>
                            </ul>
                            <p>NOTE: In certain cases, other information or documents may be requested if required during the onboarding process, e.g.</p>
                            <ul>
                                <li>Certification of Documents</li>
                                <li>A copy of the Business Plan, A Legal Opinion of the business model</li>
                                <li>Meeting in person, Video call, A Selfie with your passport</li>
                                <li>CV / Resume, Professional References</li>
                                <li>Source of Funds declaration</li>
                                <li>An Additional Proof of Identity and Proof of Address</li>
                            </ul>
                            <h3>Supporting Documents Guideline:</h3>
                            <ul>
                                <li>
                                    <strong>POI</strong> - Proof of ID (valid passport or other government issued photo ID in colour)
                                    <strong>POA</strong> - Proof of Residential Address (utility bill or bank statement dated within the last 3 months clearly showing the
                                    Name and physical Address, (P.O. box address is not acceptable)
                                </li>
                                <li>
                                    All Documents must be in English. Where translated, they must be certified by a competent authority.
                                </li>
                            </ul>
